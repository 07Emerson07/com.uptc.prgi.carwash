public class Vehicles {
    private String licencePlate;
    private TypeVehicle typeVehicle;
    private Service serviceOne;
    private Service serviceTwo;
    private Service serviceTwo;

    public Vehicle(String licencePlate, TypeVehicle typeVehicle, Service serviceOne, Service serviceTwo, Service serviceTwo) {
        this.licencePlate = licencePlate;
        this.typeVehicle = typeVehicle;
        this.serviceOne = serviceOne;
        this.serviceTwo = serviceTwo;
        this.serviceTwo = serviceTwo;
    }

    public void setLicencePlate(String licencePlate) {
        this.licencePlate = licencePlate;
    }

    public void setTypeVehicle(TypeVehicle typeVehicle) {
        this.typeVehicle = typeVehicle;
    }

    public void setServiceOne(Service serviceOne) {
        this.serviceOne = serviceOne;
    }

    public void setServiceTwo(Service serviceTwo) {
        this.serviceTwo = serviceTwo;
    }

    public void setServiceTwo(Service serviceTwo) {
        this.serviceTwo = serviceTwo;
    }

    public String getLicencePlate() {
        return licencePlate;
    }

    public TypeVehicle getTypeVehicle() {
        return typeVehicle;
    }

    public Service getServiceOne() {
        return serviceOne;
    }

    public Service getServiceTwo() {
        return serviceTwo;
    }

    public Service getServiceTwo() {
        return serviceTwo;
    }
    
}
